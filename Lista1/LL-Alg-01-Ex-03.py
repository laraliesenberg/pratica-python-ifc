#área de uma sala
#pedir comprimento da largura de uma sala
largura = float(input("Qual a largura da sala em metros?"))
#pedir comprimento da profundidade da sala
profundidade = float(input("Qual a profundidade da sala em metros?"))
area = largura*profundidade
#exibir área da sala
print("A área da sala é", area, "metros quadrados")
