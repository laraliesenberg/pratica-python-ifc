#Bugigangas e quinquilharias
#Pedir quantidade de bugigangas
bugigangas = int(input("Qual a quantidade de bugigangas que você tem?"))
#Pedir quantidade de quinquilharias 
quinquilharias = int(input("Qual a quantidade de quinquilharias que você tem?"))
#Calcular peso total do pedido
total = (bugigangas*75)+(quinquilharias*112)
#Exibir peso total do pedido
print("O peso total do pedido é", total, "gramas")